local function revertLava()
	for i = 1, #itemsRoom do
		local item = itemsRoom[i]
		if isInArray(tiles, item.itemid) then
			local ground = Tile(item.position):getGround()
			local transformid = item.itemid
			if ground then
				ground:transform(transformid)
				ground:setActionId(34201)
			end
		else
			local tp = Game.createItem(item.itemid, 1, item.position)
			tp:setActionId(34201)
		end
	end
	return true
end
local function lavaGrounds (creature)
	local summoncount = creature:getSummons()
	if #summoncount < 4 then
		for i = 1, 4 do
			local mid = Game.createMonster("Rage of Mazoran", Position(math.random(33576, 33593), math.random(32684, 32695), 14), true, true)
			if not mid then
				return
			end
			mid:setMaster(creature)
		end
	end
	for x = 33572, 33598 do
		for y = 32679, 32701 do
			local position = Position(x, y, 14)
			local tile = Tile(position)
			if not tile then
				return
			end
			local ground = tile:getGround()
			if not ground then
				return
			end
			local grounds = ground:getId()
			if grounds ~= 919 then
				local items = tile:getItems()
				for i = 1, #items do
					local item = items[i]
					local itemId = item:getId()
					if not isInArray(Montains, itemId) then
						item:remove()
					end
				end
				ground:transform(23863)
				ground:setActionId(34200)
			end
		end
	end
	addEvent(revertLava, 5 * 1000)
end
function onCastSpell(creature, var)
	creature:say('THE GROUND BEGINS TO HEAT UP RAPIDLY!', TALKTYPE_MONSTER_YELL)
	addEvent(lavaGrounds, 3 * 1000, creature)
	return
end
