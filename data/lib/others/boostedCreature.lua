function BoostedCreature:startup()
	local lastDate = os.date("*t", getGlobalStorageValueDB(self.storages.timestamp))
	local currentDate = os.date("*t", os.time())
	if not lastDate or lastDate.day ~= currentDate.day or lastDate.month ~= currentDate.month then
		-- roll new bonus creature
		local cloneTable = {}
		for name, raceId in pairs(self.races) do
			cloneTable[#cloneTable + 1] = {name, raceId}
		end

		local lastRaceId = getGlobalStorageValueDB(self.storages.race)

		local randMonster = nil
		repeat
			randMonster = cloneTable[math.random(#cloneTable)]
		until randMonster[2] ~= lastRaceId

		setGlobalStorageValueDB(self.storages.timestamp, os.time())
		setGlobalStorageValueDB(self.storages.race, randMonster[2])
		self.name = randMonster[1]
		self:saveJson()
		return
	end

	-- just update name
	local currRaceId = getGlobalStorageValueDB(self.storages.race)
	for name, raceId in pairs(self.races) do
		if raceId == currRaceId then
			self.name = name
			break
		end
	end
end

function BoostedCreature:saveJson()
	local f = io.open(self.jsonPath, "w")
	if not f then
		print("failed to save json file for boosted creature")
		return
	end

	f:write(("{ \"boostedcreature\": true, \"raceid\": %d"):format(self.races[self.name]))
	f:close()
end
